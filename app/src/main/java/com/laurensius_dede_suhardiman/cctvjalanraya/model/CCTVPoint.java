package com.laurensius_dede_suhardiman.cctvjalanraya.model;

public class CCTVPoint {

    String idPoint;
    String idRegion;
    String namaPoint;
    String urlStream;
    String type;

    public CCTVPoint(){}

    public String getIdPoint() {
        return idPoint;
    }

    public String getIdRegion() {
        return idRegion;
    }

    public String getNamaPoint() {
        return namaPoint;
    }

    public String getUrlStream() {
        return urlStream;
    }

    public String getType() {
        return type;
    }
}
